import psycopg2
import os
from dotenv import load_dotenv

load_dotenv()

conn = psycopg2.connect(
    dbname=os.getenv("DB_NAME"),
    user=os.getenv("DB_USER"),
    password=os.getenv("DB_PASS"),
    host=os.getenv("DB_HOST"),
    port=os.getenv("DB_PORT")
)
cur = conn.cursor()
cur.execute("SELECT now(), (SELECT count(*) FROM information_schema.tables WHERE table_schema='public');")
print(cur.fetchall())
cur.close()
conn.close()
